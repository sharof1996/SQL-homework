Данные (Data) — это любая информация, представленная в цифровой форме, которую можно хранить и обрабатывать (например, числа, тексты, даты).

База данных (Database) — организованное хранилище данных, в котором информация структурирована так, чтобы её было удобно хранить, извлекать и изменять.

Реляционная база данных (Relational Database) — база данных, в которой информация хранится в виде связанных таблиц (relations). Связи определяются с помощью первичных и внешних ключей.

Таблица (Table) — основная структура хранения данных в реляционной базе данных, состоящая из строк (записей) и столбцов (полей).

--2
Пять ключевых особенностей SQL Server:

Высокая надёжность и производительность — поддержка больших объёмов данных.

Безопасность — встроенные механизмы аутентификации, авторизации и шифрования.

Масштабируемость — возможность масштабировать решения от локальных серверов до облачных.

Интеграция с другими продуктами Microsoft — Power BI, Excel, .NET, Azure.

Инструменты администрирования и разработки — SQL Server Management Studio (SSMS), SQL Server Data Tools и др.

--3
Режимы аутентификации в SQL Server:

Windows Authentication (проверка через учётные данные Windows).

SQL Server Authentication (проверка через логин и пароль, созданные в SQL Server).

